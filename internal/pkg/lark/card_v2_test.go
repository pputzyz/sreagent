func Test_SeverityToTemplate(t *testing.T) {
	assert.Equal(t, "red", SeverityToTemplate("critical"))
	assert.Equal(t, "orange", SeverityToTemplate("error"))
	assert.Equal(t, "yellow", SeverityToTemplate("warning"))
	assert.Equal(t, "blue", SeverityToTemplate("info"))
	assert.Equal(t, "grey", SeverityToTemplate("unknown"))
}

