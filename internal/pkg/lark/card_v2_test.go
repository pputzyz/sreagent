func Test_CardV2Builder_BasicBuild(t *testing.T) {
	card, err := NewCardV2Builder().
		Header("Test Alert", "red").
		AddMarkdown("**CPU** usage high").
		AddDivider().
		AddActions(NewButton("Acknowledge", "callback", "primary", map[string]interface{}{"action": "ack", "event_id": 1})).
		Build()

	require.NoError(t, err)
	assert.Equal(t, "2.0", card.Schema)
	assert.NotNil(t, card.Header)
	assert.Equal(t, "Test Alert", card.Header.Title.Content)
	assert.Equal(t, "red", card.Header.Template)
	assert.NotNil(t, card.Body)
	assert.Len(t, card.Body.Elements, 3) // markdown + divider + action
}

