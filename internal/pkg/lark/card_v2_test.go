func Test_CardV2Builder_ComponentLimit(t *testing.T) {
	b := NewCardV2Builder().Header("Big", "red")
	for i := 0; i < 201; i++ {
		b.AddMarkdown("line")
	}
	_, err := b.Build()
	assert.Error(t, err)
	assert.Contains(t, err.Error(), "201 elements")
}

func Test_CardV2Builder_30KBLimit(t *testing.T) {
	b := NewCardV2Builder().Header("Big", "red")
	bigContent := strings.Repeat("x", 30*1024)
	b.AddMarkdown(bigContent)
	_, err := b.Build()
	assert.Error(t, err)
	assert.Contains(t, err.Error(), "30")
}

func Test_CardV2Builder_BuildJSON(t *testing.T) {
	jsonStr, err := NewCardV2Builder().
		Header("Test", "blue").
		AddMarkdown("Hello").
		BuildJSON()

	require.NoError(t, err)
	assert.True(t, strings.Contains(jsonStr, `"schema":"2.0"`))

	var raw map[string]interface{}
	require.NoError(t, json.Unmarshal([]byte(jsonStr), &raw))
	assert.Equal(t, "2.0", raw["schema"])
}

